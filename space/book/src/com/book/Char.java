package com.book;

public class Char {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int a=3;
		char b=(char) (a+'0');
		System.out.println(b);
		char c='4';
		int d=c-'0';
		System.out.println(d);
		
		int e=4;
		String string=String.valueOf(e);
		System.out.println(string);

	}

}
